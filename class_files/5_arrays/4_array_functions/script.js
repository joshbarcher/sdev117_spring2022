//push() will add an element to the end of an array
let movies = ["Godzilla", "Avengers", "True Romance", "The Godfathers"];
console.log(movies);

//pop() will remove an element from the end of an array
let consonants = ["b", "c", "d", "f", "g", "h"];
console.log(consonants);

//slice() selects elements from an array
let states = ["WA", "CA", "OR", "NV", "MO"];
console.log(states);

//splice() inserts new elements into an array
let letters = ["theta", "gamma", "omega", "alpha", "lambda"];
console.log(letters)
